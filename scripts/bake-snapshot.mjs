// Bakes the agent's current /api/state into a Vercel serverless function so
// the public dashboard always renders a full (honestly labeled) session even
// when the free-tier backend is suspended or asleep.
// Usage: node scripts/bake-snapshot.mjs   (agent must be running locally)
import { writeFileSync, mkdirSync } from "node:fs";

const AGENT = process.env.STRIKER_HOST ?? `http://localhost:${process.env.AGENT_PORT ?? 4042}`;
const res = await fetch(`${AGENT}/api/state`);
if (!res.ok) throw new Error(`agent responded ${res.status} — start it with: npm run agent`);
const state = await res.json();

state.snapshotAt = new Date().toISOString();
state.agent.loopError = null;

const body = `// AUTO-GENERATED by scripts/bake-snapshot.mjs — do not edit.
// Fallback for the public dashboard when the live backend is unreachable:
// the agent's last verified session, served from Vercel itself.
const snapshot = ${JSON.stringify(state)};

export default function handler(req, res) {
  res.setHeader("Access-Control-Allow-Origin", "*");
  res.setHeader("Cache-Control", "public, max-age=60");
  res.status(200).json(snapshot);
}
`;

mkdirSync("api", { recursive: true });
writeFileSync("api/state.js", body);
console.log(`baked api/state.js — ${state.ledger.length} ledger rows, ${state.insights.length} insights, snapshotAt ${state.snapshotAt}`);
